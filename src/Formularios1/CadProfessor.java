    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        try {
            PreparedStatement pst=conecta.con.prepareStatement("insert into cad_prof(nome,codigo)values(?,?)");
            pst.setString(1,jTextFieldNome.getText());
            pst.setString(2,jTextFieldCod.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane,"Cadastrado com sucesso");
            
        } catch (SQLException ex) {
            Logger.getLogger(CadProfessor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,"Erro no cadastro/n Erro:"+ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

