    private void jButtonExActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExActionPerformed
        try {
            PreparedStatement pst=conecta.con.prepareStatement("delete from cad_prof where nome=?");
            pst.setString(1,jTextFieldNome.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"Excluído com sucesso");
            
        } catch (SQLException ex) {
            Logger.getLogger(CadProfessor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Não foi possivel excluir\n Erro:"+ex.getMessage());
        }
        jTextFieldNome.setText("");
    }//GEN-LAST:event_jButtonExActionPerformed

